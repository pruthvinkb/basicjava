package com.javabasic;

public class SwitchStstement {
public static void main(String[] args) {
	System.out.println("switch satement");
    int choice=1;
    switch (choice) {
    case 1:
    	System.out.println("case 1");
    	break;
    case 2:
    	System.out.println("case 2");
    	break;
    	default:
    		System.out.println("default");
    }
}
}
